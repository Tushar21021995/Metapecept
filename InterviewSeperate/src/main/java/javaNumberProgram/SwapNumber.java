package javaNumberProgram;

public class SwapNumber {

	public static void main(String[] args)
	{
		int i=20;
		int j=10;
		i=i+j;
		j=i-j;
		i=i-j;
		System.out.println("i=" +i);
		System.out.println("j=" +j);
		
		// using 3rd variable
		
		int a=30;
		int b=40;
		int c;
		
		c=a;
		a=b;
		b=c;
		System.out.println("a= "+a);
		System.out.println("b= "+b);
		
		
		
		
		
		
	}

}
